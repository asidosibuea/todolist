public class Kegiatan {
    private String kegiatan, waktu;

    public Kegiatan(String kegiatan, String waktu) {
        this.kegiatan = kegiatan;
        this.waktu = waktu;
    }

    public String getKegiatan() {
        return kegiatan;
    }

    public String getWaktu() {
        return waktu;
    }

}
